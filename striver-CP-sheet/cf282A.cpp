#include<bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin>>n;
    int num = 0;
    for(int i=0;i<n;i++){
        string s;
        cin>>s;
        if(s[1] == '+'){
            num++;
        }
        else{
            num--;
        }
    }
    cout<<num<<endl;
    return 0;
}
